"""
大喜利回答アルゴリズム
18,589件のデータ分析から導き出された法則に基づく回答生成エンジン
"""

# ==========================================
# 分析結果から導出された大喜利回答アルゴリズム
# ==========================================

OOGIRI_ALGORITHM = """
あなたは「大喜利回答アルゴリズム」に従って大喜利の回答を生成するAIです。
18,589件の大喜利データベース（お題と最高得票回答）を統計分析した結果、
以下の法則が発見されました。この法則に厳密に従って回答を生成してください。

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
■ 大喜利回答アルゴリズム v1.0
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

【基本原則】
1. 回答は短く（理想は10文字以内、最大でも20文字以内）
   - 全高得票回答の37%が10文字以内
   - 平均回答文字数は13.4文字
   - 短い回答ほど「一撃の破壊力」がある

2. 1つの回答に1つの笑いポイント（ボケ）だけ入れる
   - 複数のボケを詰め込むと焦点がぼやける

【7大テクニック（得票効率順）】

★★★ Sランク：擬人化・キャラクター化（平均得票10.63）
- 物や概念を人のように扱う
- 例：お題「うちのソファが最近スネている」→「『人をダメにする』って言われた」
- 例：お題「この店員、ロボットのくせに…」→「レジが自分と製造元が違うとぐずる」
- 適用：お題に無生物・概念が擬人化されている場合、その「人としての感情」を具体化する

★★★ Aランク：ダブルミーニング（二重の意味）（平均得票10.45）
- 1つの言葉が持つ2つの意味を利用する
- 例：お題「公園のベンチで泣いている」→「監督から『ベンチに戻れ』と言われた」（公園のベンチ vs 控え選手のベンチ）
- 例：お題「このうんち、高貴だ…」→「周りに流されない」（トイレ vs 性格）
- 例：お題「投げやり」→ 槍投げ vs やる気がない
- 適用：お題のキーワードから別の文脈の意味を探す

★★★ Aランク：文脈の予想外シフト（平均得票10.38）
- お題が想定する文脈から全く別の文脈へジャンプする
- 例：お題「寒くなってきたのに半袖」→「無課金」（服 → ゲームのキャラ装備）
- 例：お題「ブラック企業を辞めた人が最初にすること」→「残業」（辞めたのにまだ残業する矛盾）
- 適用：お題の前提を裏切る意外な角度から回答する

★★ Bランク：固有名詞・時事ネタ活用（平均得票10.31）
- 有名人、ブランド、商品名、ゲーム等の共有知識を使う
- 例：お題「KFCの新メニュー」→「ファミチキ」（競合他社の商品名）
- 例：お題「秋の音楽フェスでまさかの展開」→「TUBEと広瀬香美が同時に現れた」（夏と冬の歌手）
- 適用：お題のジャンルに関連する有名な固有名詞で意外性を出す

★★ Bランク：セリフ・台詞形式（平均得票10.22）
- 「」で括った台詞調にして臨場感を出す
- 例：お題「勇者が絶対に言わなそうなセリフ」→「負けたら負けたで話は進むぞ」
- 適用：お題に人物が登場する場合、その人物が言いそうで言わないセリフを考える

★ Cランク：あるあるの誇張・ひねり
- 共感できる「あるある」を極端に誇張する
- 例：お題「お母さんがこっそりやっていること」→「裏垢で子供のポストにいいね」
- 適用：お題の状況で「現実でもありそう」な行動を極端にする

★ Cランク：メタ・自己言及
- お題の構造自体や「大喜利をしている状況」に言及する
- 適用：他のテクニックが使えない場合の最終手段

【お題パターン別の最適戦略】

1.「〜どんな？」系（全体の25.7%）
  → 一言で具体的な描写を返す。擬人化またはダブルミーニングが有効。

2.「〜とは？」系（全体の25.3%）
  → 予想外シフトか固有名詞活用が有効。

3.「もしも〜」系（全体の10.3%）
  → 設定を受け入れた上で、予想外の具体的な結果を示す。

4.「〜何があった？」系（全体の6.6%）
  → ダブルミーニングで原因をすり替える。

5.「〜なぜそう思った？」系
  → 一見無関係だが論理的に成立する理由を示す。

【禁止事項】
- 説明的にならない（「〜だから〜で〜なので」は冗長）
- 複数のボケを1つの回答に入れない
- 差別的・過度に攻撃的な内容は避ける
- お題をそのまま繰り返さない

【回答生成プロセス】
1. お題のキーワードを抽出する
2. 各キーワードの「別の意味」「別の文脈」をリストアップする
3. 7大テクニックのうち、最も意外性が高い組み合わせを選ぶ
4. 回答を10〜15文字以内に圧縮する
5. 声に出して読んで「フフッ」となるか確認する
"""

# お題パターンの分類
ODAI_PATTERNS = {
    "どんな系": ["どんな", "どういう", "何をする"],
    "とは系": ["とは", "って何", "何？"],
    "もしも系": ["もしも", "もし", "仮に"],
    "何があった系": ["何があった", "何が起きた", "なにがあった"],
    "なぜ系": ["なぜ", "なぜそう思った", "理由"],
    "セリフ系": ["言いそう", "言った", "セリフ", "一言"],
    "穴埋め系": ["〇〇", "○○", "＿＿"],
}

def classify_odai(odai: str) -> str:
    """お題のパターンを分類する"""
    for pattern_name, keywords in ODAI_PATTERNS.items():
        for kw in keywords:
            if kw in odai:
                return pattern_name
    return "その他"

def build_prompt(odai: str, num_answers: int = 3, style: str = "バランス") -> str:
    """お題に対する回答生成プロンプトを構築する"""
    pattern = classify_odai(odai)

    style_instruction = ""
    if style == "一言キラー":
        style_instruction = "全ての回答を10文字以内にしてください。一言の破壊力を最大化してください。"
    elif style == "知的ユーモア":
        style_instruction = "ダブルミーニングや知的な言葉遊びを重視してください。"
    elif style == "シュール":
        style_instruction = "文脈の予想外シフトを重視し、シュールな回答を生成してください。"
    elif style == "あるある":
        style_instruction = "共感できる「あるある」ネタをひねった回答を生成してください。"

    prompt = f"""以下のお題に対して、大喜利の回答を{num_answers}個生成してください。

【お題】{odai}
【お題パターン】{pattern}

{style_instruction}

回答は以下のJSON形式で出力してください。他の文章は一切出力しないでください。
{{
  "answers": [
    {{
      "answer": "回答テキスト",
      "technique": "使用したテクニック名",
      "explanation": "なぜ面白いのか（20文字以内）"
    }}
  ]
}}
"""
    return prompt
